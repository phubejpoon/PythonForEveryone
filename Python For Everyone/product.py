product = {'ดินสอกดpental':{'ราคา':50,'สี':'แดง'},'ยางลบ':{'ราคา':3,'สี':'เขียว'},'ดินสอสี':100,'สี':'ส้ม'}


while True:
    print('----กรุณากรอกข้อมูลสินค้า----')
    p = input('ซื่อสินค้า: ')
    q = int(input('จำนวน: '))
    print('-----')
    if p in product:
        print(f'สินค้า: {p} \nราคา: {product[p]['ราคา']} บาท \nสี: {product[p]['สี']}')
        print(f'จำนวน: {q} ขิ้น \nรวมทั้งหมด: {product[p]['ราคา'] * q} บาท')
    else:
        print('ไม่มีสินค้าในระบบ')